public class StringExercise {
    public static void main(String[] args) {
        String input1 = "We don't need no education\nWe don't need no thought control";
        System.out.println(input1);
        String input2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(input2);
        String input3 = "In windows, the main drive is usually C:\\";
        System.out.println(input3);
        String input4 = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(input4);
    }
}
